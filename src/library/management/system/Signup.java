    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String sql = "insert into account(Username,Name,Password,Sec_Q,Answer)values(?,?,?,?,?) ";
        try {
            String passEncrypt = DigestUtils.md5Hex(txtPass.getText());
            ps = con.prepareStatement(sql);
            ps.setString(1, txtUsername.getText());
            ps.setString(2, txtName.getText());
            ps.setString(3, passEncrypt);
            ps.setString(4, (String) txtSec_Q.getSelectedItem());
            ps.setString(5, txtAnswer.getText());
            ps.execute();

            JOptionPane.showMessageDialog(null, "Đăng kí tài khoản thành công");

            txtUsername.setText("");
            txtName.setText("");
            txtPass.setText("");
            txtAnswer.setText("");
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Signup.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

