    public void Search() {
        String account = txtAccount.getText();
        String sql = "select * from account where Username='" + account + "'";
        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                txtName.setText(rs.getString(2));
                txtSec_Q.setText(rs.getString(4));
            } else {
                JOptionPane.showMessageDialog(null, "Tài khoản không tồn tại trong hệ thống");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ForgotPassword.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public boolean checkAnswer() {
        String answer = txtAnswer.getText();
        String sql = "select * from account where Answer='" + answer + "'";
        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                return true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ForgotPassword.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

    public void resetPass() {
        String account = txtAccount.getText();
        String passEncrypt = DigestUtils.md5Hex(txtPass.getText());
        String sql = "update account set Password='" + passEncrypt + "'" + "where Username='" + account + "'";
        try {
            ps = con.prepareStatement(sql);
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(ForgotPassword.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

